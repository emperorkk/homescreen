function key(date: Date): string {
  const m = String(date.getMonth() + 1).padStart(2, "0");
  const d = String(date.getDate()).padStart(2, "0");
  return `${m}-${d}`;
}

/** Names celebrating on the given date (may be empty). */
export function nameDaysOn(date: Date): string[] {
  return NAMEDAYS[key(date)] ?? [];
}
